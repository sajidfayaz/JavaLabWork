public class ComTwoNumbers {

  public static void main(String[] args) {
    int a = 5, b = 1;
    if (a == b) {
      System.out.println("Both are equal");
    } else if (a > b) {
      System.out.println("a is greater than b");
    } else {
      System.out.println("b is greater than a");
    }
  }
}
